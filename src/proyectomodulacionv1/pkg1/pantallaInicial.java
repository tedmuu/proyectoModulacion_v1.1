    private void jButtonModActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModActionPerformed

        
        
        
        
        
        double vc,vm,fc,fm, k,tiempo;
        vc=Double.valueOf(jText_VcPm.getText());
        vm=Double.valueOf(jText_VmPm.getText());
        fc=Double.valueOf(jText_FcPm.getText());
        fm=Double.valueOf(jText_FmPm.getText());
        k=Double.valueOf(jText_K.getText());
        tiempo=Double.valueOf(jText_Tiempo.getText());
        
        
//        graficando portadora modulada
        JPPortadorasModulacionCosPm PMPM = new JPPortadorasModulacionCosPm(vc,vm,fc,fm, k, tiempo);
        PMPM.setSize(893,100);
        jPanel_Sin_modular.removeAll();
        jPanel_Sin_modular.add(PMPM,BorderLayout.CENTER);
        jPanel_Sin_modular.revalidate();
        jPanel_Sin_modular.repaint();
        
//        se√±al moduladora
          JPModuladoraCosCos objModuladoraPM = new JPModuladoraCosCos(vc,vm,fc,fm, k,tiempo);
        objModuladoraPM.setSize(893,100);
        jPanel_S_Moduladora.removeAll();
        jPanel_S_Moduladora.add(objModuladoraPM,BorderLayout.CENTER);
        jPanel_S_Moduladora.revalidate();
        jPanel_S_Moduladora.repaint();


    }//GEN-LAST:event_jButtonModActionPerformed

    
    
    
    
    
    
    
    
    
    
